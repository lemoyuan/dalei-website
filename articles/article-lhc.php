							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Majorana（马约拉纳）和Gerda（锗探测列阵）其实都是女子名，它们指的是两个欧、美联合项目。这两个项目都在“搜寻反中微子”（确认中微子是否就是自己的反粒子，可发生“无中微子双β衰变”）—在物理学中，目前尚未发现任何支持反中微子存在的证据。但是如果得到证实，可以将科学的发展向前推进数十年。因为这可以解释宇宙中暗物质的组成，从而揭开自然科学史上的一大迷题。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;尽管历经多年，无论马约拉纳研究还是锗探测列阵研究都没拿出有力的结果。德国之声去年曾在一篇评论中称，不过在中国人还没有加入这一研究前，这不算太糟糕。然而现在，中国人不但加入而且似乎超过了西方—至少他们已经为此创造了最好的条件。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;梅东明是建造反中微子探测器最有声望的专家之一，本已在美国工作多年。然而当听说自己的祖国也开始进行这项研究后，他回国了。谁能为此责怪他呢？他的美国同事此前做梦也没想到，中国有一天会成为比美国更吸引人的研究地。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;要想证明反中微子的存在，还需要另一种极难提取的物质—锗。梅东明这些研究人员用高纯度锗制造的探测器要追踪反中微子，还需要一个特别的环境。这个环境不仅需要最先进的电子设备，也需要极度纯净的技术，光是实验人员身上的微尘粒就能导致结果有误。在这些问题上，中国人需要帮助。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;更重要的是这样的实验必须完全避过各种宇宙辐射，因此搜寻反中微粒子的实验室必须建在地下深处。所有这些，人们都一直没想到中国人能做到。然而如今，他们在四川省的深山区开始挖建世界最深的实验室（中国锦屏地下实验室—观察者网注，由清华大学与雅砻江流域水电开发有限公司共建）。其中的一厅已经建成，剩余的七厅预计在2018年前完工。最晚到那个时候，中国人就将拥有比意大利中部的、领先世界的大萨索山实验室更先进的地下实验室。大萨索山地下实验室现在整体陷入停滞：西方研究人员很难找到用于修建微子探测器的5亿欧元，因此他们羡慕地望着东方—在那里，钱不是问题。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个东方实验室在一定程度上，算是雅砻江八万人施工的世界最大围堰的副产品。相比之下，大萨索山的欧洲地下实验室看上去像是间游戏室。针对微子物理学家担忧的宇宙射线，在四川山下要比在大萨索山下弱200倍。四川实验室还将前所未有得装备一个由1000公斤高纯度锗制造的探测器—这至少耗资5亿欧元。中国已经是一个高科技国家，而且正在成为一个航天大国。如今，中国也要在最顶尖的基本粒子物理学中立足。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;欧洲人，其中也包括慕尼黑马克斯·普朗克物理研究所的研究人员，已被野心勃勃的中国人所吸引，以至于他们新近一致决定和中国人合作—而且是在中国人的领导下。而美国人则更持怀疑态度，他们最多愿意充当顾问。对于这项研究而言，更好的当然是大家一起搜寻。然而这对于正处于上升和下降中的大国代表们来说很难。欧洲和美国合作的非常不错，而这也体现了在一个多元化的世界秩序下，应该共同解决世界大问题，而这就要求每位参与者都学着退一步。奖励将是一个由美、欧、中研究者共同获得的诺贝尔物理奖以及一个政治上的巨大进步。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 2013年3月9日消息，美国“万亿伏特粒子加速器”(Tevatron)项目的科学家们近日表示他们在数据中发现了多个疑似希格斯玻色子的信号，其质量数大致和欧洲大型强子对撞机(LHC)得到的结果相同。这一发现增加了这样一种说法的分量，那就是希格斯粒子可能存在于125吉电子伏特质量数附近。这种神秘粒子被认为赋予了所有其它粒子以质量。然而，这些新的数据本身仍然不足以从统计学角度上增加这项发现的可靠性。目前研究人员们已经将他们的结果在近日于意大利举行的物理会议上公布。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;“万亿伏特粒子加速器”(Tevatron)隶属于美国费米国家实验室，它在长达20年内一直占据着全球最强大粒子加速器的位置，但是在2011年却因为延长其资金拨款的谈判失败而被迫停止运行。然而和所有其它粒子加速器一样，Tevatron在其运行过程期间已经积累下海量的数据需要分析。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;最新的数据暗示希格斯粒子可能存在于115~135 GeV之间，置信度为2.2。这一数值是质子质量的120~140倍，质子是原子核的基本组成部分，它存在于每一种原子的原子核内。在物理学上，这种置信度就意味着有1/36的几率这种信号是一个误报或者噪音，这一数字是远远低于5这个被视作可宣布为“正式发现”的物理学黄金标准的。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而让这一发现更加显出其重要意义的一件事情在于，尽管两者采用的方法大相径庭，但是欧洲的大型强子对撞机同样在其数据中找到一个峰值，并且这个峰值出现的质量位置恰好和Tevatron的结果非常接近。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;LHC的做法是将质子进行对撞，而Tevatron则使用质子和它们的反物质粒子，即反质子进行对撞。这两项实验的目的都是想通过观察这些高能粒子在衰变后的情况来搜寻希格斯粒子的踪迹。在Tevatron，这些数据是由底夸克和它的反物质伙伴反底夸克对撞产生的，而在LHC，主要的探测目标则是对撞时产生的光子。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;鲍勃·罗瑟(Rob Roser)说：“这是一台不同的加速器，不同的探测器，不同的衰变过程。”鲍勃是CDF探测器项目的发言人，这是安装在Tevatron设备上的两台主要探测器之一。他告诉BBC记者说：“它正在让图像变得清晰起来，让事情变得更加有说服力。但是我们还没有达到我们期望的那样自信。”他说：“我只是希望我们两家中至少有哪一家能有更多的数据，这真让人沮丧。”而在LHC，两台安装在其中的主要探测器CMS和Atlas同样在周三举行的会议上报告了他们的进展，但是相比去年他们报告的内容，今年这一次的报告中仅仅提供了些许更进一步的数据。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;但是这一切的艰难困境在今年下半年将会被一扫而空，因为科学家们计划今年将获得相当于2011年全年3倍的数据量。然而，近期对于Atlas数据进行的分析结果已经排除了122.5 GeV以上区间的可能性。Tevatron的数据则帮助科学家们排除了质量超过147-179 GeV的情形，这一数据同样和LHC的结果相当吻合。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;正如先前所言的那样，如果希格斯玻色子真的存在，那么它可能存在的范围已经被缩小了。
 </p>
 </p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;托尼·魏伯格(Tony Weidberg)是一位牛津大学的物理学家，他在LHC的Atlas探测器项目工作，他说Tevatron的探测结果符合一种“较轻”的希格斯玻色子的设想。
 </p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;他告诉BBC记者说：“这很有趣，因为这是另一个小小暗示。它暗示我们到今年年底时将有可能发现这种粒子，而不是宣布排除了这种可能性。”他说：“证据就隐藏在LHC的数据中，我们今年就将找到它；到今年年底，我们将走出模糊和不确定，得到清晰的证实或排除的结果。对我来说，不管结果如何，这两个结果中其中任意一个的确认都将让人兴奋不已。”</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;粒子加速器（particle accelerator）是用人工方法产生高速带电粒子的装置。日常生活中常见的粒子加速器有用于电视的阴极射线管及X光管等设施。是探索原子核和粒子的性质、内部结构和相互作用的重要工具，在工农业生产、医疗卫生、科学技术等方面也都有重要而广泛的实际应用。</p>
