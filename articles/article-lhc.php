							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;按规划，欧洲大型强子对撞机第二阶段运行可实现每个质子束流产生6.5万亿电子伏特的能量，进而在今夏实现以创纪录的13万亿电子伏特的质子束流总能量进行对撞实验。公报说，未来数天科研人员将会在检查各系统状况后逐步提升质子束能量。
 </p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;公报说，在对撞机停机期间，欧核中心强化了对撞机的1万个超导磁铁连接点，安装了超导磁铁保护系统，同时改进增强了制冷、真空及电子系统。随着今夏将以13万亿电子伏特的质子束流总能量运行，欧洲大型强子对撞机实验将探索希格斯玻色子机制、暗物质、反物质等更多未知领域。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;欧洲大型强子对撞机于2008年9月建成运行，首阶段运行于2012年末结束。该阶段的两个强子对撞实验项目ATLAS和CMS均证实了“上帝粒子”希格斯玻色子的存在，理论上认为希格斯波色子是构成宇宙的最基本组成部件之一，解释了为何其它粒子会拥有质量。</p>
 
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根据这一理论，在宇宙大爆炸后，希格斯场和与之对应的粒子—希格斯玻色子一同形成。而希格斯场，这种看不见的力赋予了其他基本粒子质量的属性。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;　研究者们想要借此揭开有关宇宙基本组成的更多迷题，他们希望能在未来几年首次获得暗物质存在及其组成的实际证据。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;德国电子同步加速器研究所（DESY）所长姆尼克（Joachim Mnich）表示，"大型强子对撞机以更高的能量重启，给予了进入新未知领域和发现新物理现象的机会，比如证明暗物质的存在"。</p>
 
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前DESY的150名工作人员参与了欧洲粒子物理研究所的实验项目，而世界范围内有上万名科学家参与了该实验。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;中国粒子物理学迎头赶上。</p>
