						    <h2><strong>LHC--欧洲大型强子对撞机</strong></h2>
                            <P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大镭核新技术研究部</P>
                            <div class="divider half-margins"><!-- divider -->
							    <i class="fa fa-star"></i>
						    </div>

						    <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 欧洲核子研究中心(欧核中心，CERN)的物理学家、相关部门负责人博尔德里近日在瑞士调控中心发出这样一条推特：在经历两年停机维护、加固升级及数月重启准备后，世界最大的粒子加速器—欧洲大型强子对撞机（LHC）当天正式开启第二阶段运行，物理学家们又开始新一轮的寻找神秘粒子之旅。</P>
						    <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;欧洲大型强子对撞机是目前全球最大、能量最高的粒子加速器，它通过埋入地下100米深、总长27公里的超导磁铁加速并碰撞粒子，可在微观尺度上还原宇宙大爆炸后的宇宙初期形态，帮助科学家研究宇宙起源并寻找新粒子。　</P>

						    <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大型强子对撞机是世界最大的粒子加速器，项目投资达39亿欧元(约合56亿美元)，位于日内瓦附近瑞士和法国交界地区地下100米深处、总长17英里(约合27公里)的环形隧道内，建造用以寻找暗物质、反物质等现象，最终揭开宇宙形成之谜。</p>
</P>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;重启比原计划晚了一周，因为对撞机磁路在2015年3月21日出现了故障。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;欧核中心在公报中说，欧洲大型强子对撞机于当地时间2015年4月5日上午10时41分发射出第一束质子束流，另一束相反方向的质子束流于5日中午12时27分发射，两束质子束流循环的注入能量为0.45万亿电子伏特。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;欧核中心在公报中说，欧洲大型强子对撞机于当地时间2015年4月5日上午10时41分发射出第一束质子束流，另一束相反方向的质子束流于5日中午12时27分发射，两束质子束流循环的注入能量为0.45万亿电子伏特。</p>
