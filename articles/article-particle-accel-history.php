							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  粒子加速器最初是作为人们探索原子核的重要手段而发展起来的。其发展历史概括如下：</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1919年，卢瑟福用天然放射源实现了历史上第一个人工核反应，激发了人们用快速粒子束变革原子核的
粒子加速器强烈愿望。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1928年，伽莫夫关于量子隧道效应的计算表明，能量远低于天然射线的α粒子也有可能透入原子核内。该研究结果进一步增强了人们研制人造快速粒子源的兴趣和决心。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1932年，J．D．考克饶夫特（John D. Cockroft）和E.T.瓦尔顿（Earnest T. S. Walton）在England的 Cavendish实验室开发制造了700kV高压倍加速器加速质子，即Cockroft-Walton 加速器，实现了第一个由人工加速的粒子引起的Li（p，α）He核反应。由多级电压分配器（multi-step voltage divider ）产生恒定的梯度直流电压，使离子进行直线加速。 </p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1930年，Earnest.O. Lawrence制作了第一台回旋加速器，这台加速器的直径只有10cm。随后，经M. Stanley Livingston资助，建造了一台25cm直径的较大回旋加速器，其被加速粒子的能量可达到1MeV。几年后，他们用由回旋加速器获得的4.8MeV氢离子和氘束轰击靶核产生了高强度的中子束，还首次生产出了、和等人工放射性核素。 </p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1940 由 D. W. Kerst 利用电磁感应产生的涡旋电场发明了新型的加速电子电子感应加速器（Betatrons）。它是加速电子的圆形加速器。与回旋加速器的不同之处是通过增加穿过电子轨道的磁通量（magnetic flux ）完成对电子的加速作用，电子在固定的轨道中运行。在该加速器中，必须和处理电子的相对论作用一样来处理由辐射而丢失的能量。所有被加速的粒子辐射电磁能，并且在一定动能范围内，被加速电子的辐射损失能量比质子的多。这种丢失的辐射能称同步加速辐射。因此，电子感应加速器的最大能量限制在几百MeV内。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 在研制电子感应加速器的过程中提出了电子的振荡理论，并解决了带电粒子在加速过程中的稳定性问题。该理论适用于各种类型的梯度磁场聚焦的加速器。因此，在加速器的发展历史上，该加速器起了重要的作用。电子感应加速器除了主要用于产生的γ射线做核反应等方面的应用外，还广泛用于工业和医疗方面：如无损探伤、工业辐照、放射治疗等。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1945年，维克斯勒尔和E.M.麦克米伦分别提出了谐振加速中的自动稳相原理，从理论上提出了突破回旋加速器能量上限的方法，从而推动了新一代中高能回旋谐振式加速器如电子同步加速器、同步回旋加速器和质子同步加速器等的建造和发展。</p>

