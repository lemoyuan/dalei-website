                            <div><h3><strong>关于粒子加速器你可能不知道的10件事</strong></h3></div>

                            <div id="niu2-left-container">
                                <div id="niu2-main-content">
						            <P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大镭核新技术研究部</P>

                                    <div class="divider half-margins"><i class="fa fa-star"></i></div>
                                  <h4 id="content-heading"  style="color:#ffe100">[摘要]</h4><P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;现在，欧洲核子研究中心（CERN）的大型强子对撞机（LHC）已经逐渐进入了流行文化：Jon Stewart在《每日秀》里开它的玩笑；在《生活大爆炸》里，谢耳朵表示它曾是自己的梦想；小说《天使与魔鬼》中，邪恶势力还从这里窃取了虚构的“反物质”。

虽然知名度不断提高，但粒子加速器仍有许多不为人知的秘密。在此，《symmetry》杂志就整理出了10项你可能不知道的有关于粒子加速器的小知识。</p>

                                  <h4 id="chart1"><strong>世界上正在运作的加速器超过30000台</strong></h4>
</br>
                                  <div align="center" >
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-accel1.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>
                                  <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;加速器遍布各地，从事各种各样的工作。它们最为人熟知的一面是在粒子物理科研中的作用，不过这些加速器还可以在其他很多领域发挥作用。比如，它们可以产生破坏肿瘤的射线来治疗癌症（医用直线加速器会加速电子，并产生高能X射线用于肿瘤放射治疗），通过杀菌来避免食物性疾病（一种辐照灭菌系统也用到了电子加速器），改良高分子材料的性能，并帮助科学家们开发效率更高的车辆引擎。</p>


						          <h4 id="chart2"><strong>世界上最长的现代建筑之一就是为粒子加速器而建的</strong></h4>
</br>
						          <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-accel2.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;直线加速器是在一条直线上加速粒子的设备。一般来说，直线加速器建得越长，粒子碰撞就越猛烈。斯坦福直线加速器中心（SLAC）国家加速器实验室坐落于旧金山附近，这里拥有全球最长的直线加速器。</P>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;SLAC直线加速器的坑道本身建在地下，而它的调速管走廊则建在地上，这个部分就是全世界最长的现代建筑之一。调速管走廊位于加速器的上方，这里安置了给加速器提供动力的装置。它的整体长度差不多有3千米，加速器实验室的员工们每年都会在这里举办沿着它赛跑的活动：</P>
                                  <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-malaysia.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>
 
                                  <h4 id="chart3"><strong>斯蒂芬•霍金认为，粒子加速器是人类拥有的最接近时间机器的设备</strong></h4>
</br>
                                  <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-accel3.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2010年，理论物理学家斯蒂芬•霍金为英国《每日邮报》撰稿，阐释了时间旅行的可行性。他指出，建造时间机器所需的是一个像粒子加速器那样可以加速人类的机器。</P>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果有一个与大型强子对撞机（LHC）性能相当的“人类加速器”，可以使乘客的速度接近光速的话，根据狭义相对论，对于同一段时间，机器之外的人觉得过了好几年，但加速器内的乘客会感到只经过了几天。当他们结束这次加速旅行时，会比其他人年轻许多。</P>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;当然，霍金并没有建议人类真的去制造这样一台机器。但他指出了现在已经发生的一种“时间旅行”的表现方式。比如说，π介子这种粒子在正常情况下寿命很短，不到百万分之一秒（译者注：更确切的说法是千万分之一秒，π介子的平均寿命为26纳秒）就会衰变。但是，当被加速到接近光速时，它们的寿命会大幅增加。这些粒子看起来就像是进行了“时间旅行”，至少相对于其他粒子来说，它们的时间“过得更慢了一些”。</P>

					    	      <h4 id="chart4"><strong>人类所能达到的最高温度记录是在粒子加速器中创造的</strong></h4>
</br>
					    	      <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-accel4.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2012年，布鲁克海文国家实验室创下了一项吉尼斯世界纪录。这里的研究者们在相对论重离子对撞机中达到了人为制造的最高温度——大约4万亿摄氏度。不过，科学家们在这个实验室里所做的事情可不是简单地把物体加热，而是创造了少量的夸克-胶子等离子体。夸克-胶子等离子体是一种物质状态，物理学家认为，宇宙诞生之初的短暂时间内物质就处于这种状态下。这种状态下的温度非常之高，以至于最基本的粒子之一——夸克都互相分离，变成了一锅“粒子汤”，而一般情况下自然界中存在的夸克都与其他夸克紧紧结合在一起。</p>
                                  <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此后，欧洲核子研究中心（CERN）的科学家们利用LHC也得到了夸克-胶子等离子体，并获得了更高的温度。</p>

					    	      <h4 id="chart5"><strong>大型强子对撞机的内部比外太空还冷</strong></h4>
</br>
					    	      <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-accel5.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大型强子对撞机中要用到超导磁铁，而为了让电磁铁变身超导体，就需要将它们冷却至超低温。LHC是世界上最大型的超低温系统，它的工作温度是-271.3℃（1.9K）。这里是地球上最冷的地方之一，甚至比外太空温度还要低一些——外太空的温度稳定在-270.5℃左右。</p>
 
                                  <h4 id="chart6"><strong>宇宙中自然“粒子加速器”的力量比地球上的任何人造物都大</strong></h4>
</br>
                                  <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-accel6.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;人类在地球上建造出了一些着实令人惊叹的粒子加速器。但是，要说到使粒子获得高能量，没有任何人造的粒子加速器能比得过大自然的力量。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;人类观测到的高能宇宙射线中，能量最高的是一个质子，它被加速到了3x1020电子伏特。银河系中没有任何已知的地方能够完成这样高能的加速过程。恒星爆炸后产生的冲击波对粒子的加速效果比任何人造加速器都强，但即使是这样它也没有足够大的能量使质子加速到那样的程度。科学家们现在仍在研究这种超高能宇宙射线的起源（相关阅读：“额滴神”粒子）</p>
                                  
                                  <h4 id="chart7"><strong>粒子加速器不仅仅加速粒子，还使它们的质量变得更大</strong></h4>
</br>
                                  <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-accel7.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;爱因斯坦在（狭义）相对论中预言，任何具有质量的粒子，其速度都不能超过光速 . 即便有再多的能量加到一个有质量的物体上，都不能使其达到光速极限。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在现代加速器中，粒子被加速后能够非常接近光速。例如，费米国家加速器实验室的主注入器能使质子加速到0.99997倍光速。当速度离光速越来越近时，提高粒子动能所消耗的加速器能量会不断增加。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;爱因斯坦告诉我们，一个物体的能量等于其质量乘以光速的平方（E=mc2），增加粒子能量实际上也增加了粒子的质量。换句话说，能量E越多，质量m也就越大。对于一个有质量的物体，当它的速度不断接近（但不会达到）光速时，它的质量会越来越大。</p>

   				    	          <h4 id="chart8"><strong>第一个环形加速器的直径不到5英寸</strong></h4>
</br>
   				    	          <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-accel8.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1930年时，受到挪威工程师罗尔夫•维德罗（Rolf Widerøe）的工作启发，年仅27岁的物理学家厄内斯特•劳伦斯（Ernest Lawrence）和他的学生利文斯顿（M. Stanley Livingston）在加州大学伯克利分校制造了第一台粒子回旋加速器。这个加速器腔室的直径不到5英寸（5英寸为12.7 厘米），能把氢离子加速到80000电子伏特。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1931年，劳伦斯和利文斯顿开始制造一台11英寸的加速器。这台设备能把质子加速到1MeV（1x106eV）. 当利文斯顿用电报向劳伦斯汇报时，还在最后加上了“噢耶”（“Whoopee!”）表示兴奋之情。劳伦斯又继续制造了更大的粒子加速器，并成立了劳伦斯伯克利以及劳伦斯利福摩尔实验室。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此后，粒子加速器经历了很长的发展阶段，目前它们能产生的粒子束能量远远超出了最初预计的极限。欧洲核子研究中心的大型强子对撞机，直径超过了5英里（8千米）。经过这一年的升级优化，LHC将能够把质子加速到6.5 TeV（6.5x1012eV）的能量。</p>

					    	      <h4 id="chart9"><strong>在1970年代，费米实验室养了一只貂来清理加速器</strong></h4>
</br>
					    	      <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/particle-accel9.jpg" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
						                </figure>
                                                                      
					               </div>
</br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;从1971年到1999年之间，费米实验室中的介子实验室是高能物理实验最关键的一部分。我们所处的整个宇宙依靠基本作用力维系，为了对基本作用力获得更深入的了解，科学家们开始研究介子和质子，它们是比原子更小的粒子。操作员在将粒子束从加速器输送到介子实验室时，需要通过埋在地下的几英里长的真空管道。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在开始传输粒子束之前，为了清理这些长长的管道，这个实验室请来了一位外援——一只名叫费莉西娅（Felicia）的宠物貂。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;貂喜欢穴居，它们很擅长在洞穴中爬行，这个特性使它成为了完成这项工作的最佳候选。费莉西娅的任务就是用线拖着浸泡了清洁溶液的抹布，并从长长的管子当中穿过。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;虽然费莉西娅的工作最终被经过特别设计的机器人替代了，但她在加速器的建设过程中发挥了无可替代的关键作用。而且，她所要求的报酬也很少，只要一点鸡肝、鱼头以及汉堡肉之类的食物就可以让她满足了。</p>
