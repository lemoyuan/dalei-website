					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;科学家们一般倾向于把大型粒子加速器建在地下。这能使它们避免撞击、保持稳定性，而同时也使它们不容易被人找到。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;比如说，当人们驾驶汽车行驶在加利福尼亚州北部的280号州际公路上时，他们可能不会意识到SLAC国家加速器实验室的主加速器就在自己的正下方运作着。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而在瑞士-法国交界处的乡村，居民们就生活在全世界能量最高的粒子对撞机LHC的正上方。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;数十年来，康奈尔大学的运动队员们一直在罗宾逊校友球场（Robison Alumni Fields）上进行着足球、橄榄球、长曲棍球等各种比赛，而这个球场就位于康奈尔电子储存环（CESR）正上方的40英尺（约12米）。科学家们利用环形粒子加速器研究粒子束，并为生物、材料科学及物理实验提供了X射线。</p>
					    	          
                                                             
