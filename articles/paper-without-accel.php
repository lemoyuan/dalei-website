					    	      <h4 id="chart4"><strong>煤炭可以成为清洁能源吗？可以，如果你把加速器安在烟囱里</strong></h4>
                                  </br>
  
                                      <div class="col-md-6">
					    	      <div align="center">
						           
						                <figure>
							                    <img class="img-responsive" src="../assets/images/articles/without-accel5.png" alt="直线电子加速器辐照加工应用大镭核技术成套设备" />
