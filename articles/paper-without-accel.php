   				    	          <h4 id="chart8"><strong>核反应堆可以防患于未然吗？可以，如果让粒子加速器接手</strong></h4>
                                  </br>
                                                         <div class="row">
<div class="col-md-6">
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;传统核反应堆是临界反应堆——它们会产生过量的中子，需要由控制棒吸收调节。问题是控制棒可能会因机械问题而故障，造成反应失控。加速器驱动的次临界系统可以直接控制中子的供应，而无需等中子过量之后再想办法消耗。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在加速器驱动的原子反应堆中，质子束撞击重靶核（铅或者水银）来产生“中子喷泉”，然后再利用这些中子驱动核裂变，如柯法特所说。这种设计很安全，他指出， 因为“当你关掉加速器，核反应就停止了。”加速器反应堆还可以将现存的核废料分解转化成短寿命同位素。目前这种反应堆还没有被建成，但欧洲，印度和中国都在研发这种反应堆，柯法特说道。</p>
