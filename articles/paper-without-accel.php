</br>
                                   <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;粒子加速器早在几十年前就逐渐走出实验室、渗入到了工业界，而且科学家们还在不停设想新的应用。当用于基础研究的联邦资金减少的时候，科学家就会想新办法来筹钱。罗伯特•柯法特（Robert Kephart）是伊利诺伊加速器中心费米实验室的主任，伊利诺伊州商业和经济机会部（DCEO）联手资助科学研究，并发展粒子加速器的应用。他和他的同事讲解了你大概可能没听说过的10个粒子加速器的应用：</P>

                                  <h4 id="chart1"><strong>牛奶盒子的封口，加速器干的</strong></h4>
                                  </br>

                                  <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;加速器用电磁力加速带电粒子，所得的粒子束可以沿设定的方向运动——就算它们离开了加速器也一样。当带电粒子经过一个原子，它可以同这个原子中电子相互作用，把它们从原先的轨道中踢走，同时破坏掉化学键。这样，某些化合物会分解掉，有些则会发生聚合。后者是粒子加速器被应用到工业界最早的例子，这项应用至少可以追溯到20世纪80年代：给薯片袋子和牛奶盒子封口。薯片袋子由两层用胶水粘在一起的铝箔制成。这种胶水在工业传送带上需要很长时间才能干。“永远都会是黏糊糊的。”柯法特说——但是电子束可以让这一切瞬间发生，“用加速器你可以瞬间让胶水聚合。</p>


						          <h4 id="chart2"><strong>大量天然气被浪费，加速器来搞定</strong></h4>
</br>

