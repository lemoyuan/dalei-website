					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;电子束并不是可以用来杀死有害生物的唯一带电粒子。质子可以消灭肿瘤，适合用于放疗因为它们比电子的穿透力更强。它们在穿透组织的过程中造成很少伤害，但它停下来的时候就足以杀死细胞。</p>
					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;“质子在路程终点会失去大部分能量并造成最大伤害，放射性伤害。”施尔特瑟夫说道。这让科学家可以调整加速器，将破坏力释放到肿瘤的精确位置。这个概念在1946年由费米实验室的第一任主任罗伯特•威尔逊（Robert Wilson）提出。将近半个世纪后，第一个医用质子束加速器在罗马林达大学医学中心（Loma Linda University Medical Center）开始运作。</p>
