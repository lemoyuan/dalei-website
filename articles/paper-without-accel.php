                                  <h4 id="content-heading"  style="color:#ffe100">[摘要]</h4><P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;物理学家用粒子加速器来回答基础物理学的问题——我们的宇宙是怎么来的，为什么物体具有质量，等等等等。很多粒子加速器个头巨大——在芝加哥附近费米实验室的万亿电子伏加速器（Tevatron）周长有六公里，而日内瓦的大型强子对撞机（Large Hadron Collider ，LHC）还要再大四倍——而且，它们都非常昂贵。在某些方面，它们是纯研究用仪器的典范。但是，如果你觉得这些机器在研究之外一无是处的话，你会大吃一惊的。</p>
