					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;燃烧煤炭会产生诸如氮氧化物和硫氧化物的烟道气体：NO2(二氧化氮)， NO3(三氧化氮)， SO2(二氧化硫)，和 SO3(三氧化硫)等等。这些气体会同大气中的水反应，从而变成硫酸或硝酸，最终以有毒酸雨的形式浇在地球上。但是如果这些氧化物与氨气（NH3）混合后并接受电子束的放射，它们可以转化成硫酸铵和硝酸铵，也就是普通的肥料。这个过程产生的粉尘状颗粒可以用静电式分离器或者离心式分离器收集，然后施到田里面。</p>
                                  <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;柯法特认为这个想法让煤炭有机会成为清洁能源。“即便是在对再生能源和核能最乐观的估计下，煤炭在未来的20年内至少还要提供我们所需能量的20%。”柯法特说，“这个方法能让燃煤更符合环保要求。”PAVAC是由拉尔夫•艾丁格（Ralf Edinger）成立的一家公司，该公司位于加拿大英属哥伦比亚，致力于率先建成这项技术。</p>



					    	      <h4 id="chart5"><strong> 抗生素危害鱼类？加速器变药成肥</strong></h4>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;加速器可以用来清理污泥，它可以清除引发藻类大量繁殖的氮和磷，以及那些对鱼类有害的激素和抗生素。用电子束照射污泥可以分解里面的药物，把它们变成无害的化合物。电子射线还可以将水离子化，产生H3O 和OH的自由基，创造非常利于氧化还原反应发生的环境。这种方法同样可以把复杂的药物化合物分解成基本元素，同时杀死病原体。“你可以放心地把经过辐照的污泥用在生菜地里面。”柯法特说道。</p>
                                  <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;90年代初，佛罗里达的迈阿密建造了一座用加速器清理城市垃圾的试验性工厂。尽管这座工厂可以运作，这项技术还不能解决所有问题，柯法特说，所以这个想法还没有被商业化。“购买处理城市垃圾的垃圾场需要完整（配套）的系统。”他解释道，但“现在得到工业界的资助还为时过早。”</p>

                                  <h4 id="chart6"><strong> 新电脑入手，多亏加速器</strong></h4>
                                               </br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;分解分子和摧毁病原体的DNA并不是加速器的唯一绝招——它们可还可以用来构造新的材料。计算机芯片生产依赖一项称为“掺杂”的技术，其中硼和磷离子是用加速器注入到硅层中的。这些离子带正电，所以加速器可以利用电磁场控制这些离子束的方向。然后这些离子穿透硅晶片的表面并被放置在内部的精确位置。这会改变材料的导电性。</p>
                                  
