					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;μ介子加速器可以让我们真的穿墙而“看”。μ介子——一种同电子相似的亚原子，但它的质量大得多——可以轻易地穿过厚重的金属墙壁和容器，但是会同核材料发生反应。如果一辆卡车，也许是边境巡逻人员驾驶的卡车，装有隐蔽的裂变材料，发出的μ介子穿过卡车可以产生出高能的伽马射线从而被检测到。</p>
