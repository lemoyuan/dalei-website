					    	      <h4 id="chart9"><strong>石油用完了？加速器帮你找</strong></h4>
                                                             </br>

					    	      <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;便携式中子发生器（中子管）通过一种叫做中子测井的技术勘测石油、天然气或者水资源。在勘探过程中，中子放到勘探钻井中。当加速器产生的中子穿过钻井周围的土地时，它们同各种原料的原子核发生反应。这个过程产生的伽马射线可以利用伽马射线探测器检测到。这些信号的强度反应了地下材料的类型。“通常人们要查看反应特征。”施尔特瑟夫说道，“如果有孔，那么放射出来的伽马射线会比较少。”同理，石油和水会产生不同量的伽马射线。</p>
                                      

