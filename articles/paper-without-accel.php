					    	    <P style="color:#bbb"></br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;比起石油，天然气更加难以驾驭，并且需要管道传输。所以每年都有数百万立方英尺的天然气被燃掉或被排掉而没有被传送到市场——造成了污染和浪费。</P>
					    	    <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根据西部价值项目（Western Values Project）估计，2013年美国浪费的天然气足够支持洛杉矶或者芝加哥全年用度。化学反应可以将天然气转化成液态烃或者石油，但是这个过程要求很高的温度和压力，只有大工厂才能实现。加速器可以通过用电子束打破碳氢键来实现同样的目标，从而使得天然气和重新结合成链状聚合物。这个过程据柯法特所说理论上可以奏效，但是还停留在未来技术的阶段——还没有建成的原型。</P>
                                      
