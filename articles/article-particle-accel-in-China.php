						    <h2><strong>中国四大高能物理研究装置——中国的粒子加速器</strong></h2>
                            <P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大镭核新技术研究部</P>
                            <div class="divider half-margins"><!-- divider -->
							    <i class="fa fa-star"></i>
						    </div>

						    <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  二十世纪80年代以来，我国陆续建设了四大高能物理研究装置――北京正负电子对撞机、兰州重离子加速器和合肥同步辐射装置。2000年以后，国家和地方政府合作，花费14亿元之巨兴建了大科学装置上海同步辐射光源。为什么国家要花费如此巨资，建设这高能物理研究装置呢？</P>
						    <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  随着科学技术的发展，人类对物质结构的认识是从一开始看到身边的各种物质逐渐发展到借助放大镜、显微镜、直到后来的粒子加速器、电子对撞机等，逐步深入到细胞、分子、原子和原子核深层次，每深入一步都会带来巨大的社会效益和经济效益。原子核及其核外电子的发现，带动了无线电、半导体、电视、雷达、激光、 X光的发展，而近几十年对原子核的研究，则为原子能的利用奠定了理论基础。</P>

						    <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;要想了解物质的微观结构，首先要把它打碎。粒子加速器就是用高速粒子去“打碎”被测物质，让正负电子在运动中相撞，可以使物质的微观结构产生最大程度的变化，进而使我们了解物质的基本性质。一直以来科学家们都致力于粒子加速器小型化，军事学家则希望制成能击穿钢铁的粒子枪。</p>
</P>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  粒子加速器按其作用原理不同可分为静电加速器、直线加速器、回旋加速器、电子感应加速器、同步回旋加速器、对撞机等。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;　 利用直线加速器加速带电粒子时，粒子是沿着一条近于直线的轨道运动和被逐级加速的，因此当需要很高的能量时，加速器的直线距离会很长。有什么办法来大幅度地减小加速器的尺寸吗？办法说起来也很简单，如果把直线轨道改成圆形轨道或者螺旋形轨道，一圈一圈地反复加速，这样也可以逐级谐振加速到很高的能量，而加速器的尺寸也可以大大地缩减。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;　1930年E.O.劳伦斯 在直线加速器谐振加速工作原理的启发下，提出了研制回旋加速器的建议。劳伦斯建议在回旋加速器里增加两个半圆形磁场，使带电粒子不再沿着直线运动，而沿着近似于平面螺旋线的轨道运动，这种改造使得加速器的电场不至于如此之长而导致电场能损失，是一个极富设想的设计发明。1931年建成了第一台回旋加速器，磁极直径约10厘米，用2千伏的加速电压工作，把氘核加速到80keV，证实了回旋加速器的工作原理是可行的。在1932年又建成了磁极直径为27厘米的回旋加速器，可以把质子加速到1MeV。</p>

							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;从20世纪30年代到50年代后半期的20年时间里，加速器的能量增加了几百倍到几千倍。这是因为要发现基本粒子。除了到宇宙线中去寻找外，就得到原子核内部去寻找。原子核内部存在非常强大的作用力，即：核力(nuclear force)把基本粒子紧紧地结合在一起，因此研究基本粒子需要很大的能量。随着加速器能量的增加，在实验室中所发现的基本粒子数目也增多了。 </p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;粒子加速器的规模已如同一个大型机器制造厂，其用电量相当于一个中等城市，工作人员可达数千人，有宇宙粒子制造厂之称。但是，尽管今日粒子加速器能量已经够大的了，可它仍然远远不能适应探索原子奥秘的要求，因此随着人们对原子奥秘探索的深入，粒子加速器仍会不断地改进。</p>
							<P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前为止，粒子加速器的最高能量是由欧洲大型强子对撞机LHC产生的。两束能量为6.5Tev的质子束相互碰撞，能量高达13TeV（万亿电子伏特）。</p>

