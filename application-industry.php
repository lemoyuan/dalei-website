								<div class="toggle-content">
									<p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<label style="color:#428bca">辐射化工</label>是利用电离辐射作用于物质产生的化学变化（化合、分解、交联、聚合、接枝、固化、降解等）来实现材料改性的一种新的加工方法。其与常规加工技术（冷热加工、光电加工、化学反应等）比较，主要特点是</p>
									<ul>
                              			<li><P style="color:#bbb">无须添加引发剂或催化剂</P></li>
                              			<li><P style="color:#bbb">能在常温甚至低温下进行反应</P></li>
                              			<li><P style="color:#bbb">可合成或制备出用普通方法难以获得的高性能特殊材料</P></li>
                            		</ul>

									<p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<label style="color:#428bca">辐射交联技术</label>就是在一定剂量的辐射作用下，聚合物大分子间形成化学键（即横键或交联键），导致分子量增加，并使聚合物的线状分子互相结合，把线状分子结构转变成热固性聚合物那样的网状大分子结构。这样，聚合物原有的性质就会有很大的改变。</p>
									<p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;辐射交联技术应用面广，产业规模大。目前，国外辐射化工领域规模化的产业主要有电线电缆、热收缩制品、汽车轮胎、聚烯烃发泡材料、表面涂层固化、高分子PTC器件等。这几类主导型产品约占辐射化工年总产值的95%以上，创造了可观的经济效益。纵观国内电线电缆、低温阻燃柔软热缩细管等产品辐照较多，尚存在巨大的发展潜力。</p>
									<p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;聚乙烯经辐射交联后才有一定强度，能将分解气体包埋住，产>品具有独立的微细气孔，表面柔滑美观、加工性能好，并具有优良的耐腐蚀性、消冲击性、绝缘和绝热性能。</p>
									<p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<label style="color:#428bca">辐射固化</label>与紫外固化正成为互补性技术，应用范围广，构成表面涂层加工的重要产业。辐射固化是利用低能电子束（EB）或紫外光（UV）照射液体低聚物涂层（膜）使其快速固化的技术。EB固化具有室温固化、节能高效、零污染、涂层不畸变等优点，主要加工大、厚、重的物件，与EB固化相比UV固化有投资少，成本低等竞争优势，适合加工小、薄、轻的物件。两种技术路线构成互补，目前，在日本、北美、欧洲已经形成较大的产业规模。</p>
									<p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<label style="color:#428bca">辐射聚合、辐射接枝技术</label>产业规模较小，但在新材料合成与材料改性方面发挥着重要作用。水性乳胶、离子交换膜是其中代表性的产品，特别是由辐射乳液聚合获得的水性乳胶，质纯性优，没有残存易挥发有机物，更符合环保要求。用水性乳胶调制的乳胶漆，水性家具漆、地板漆等是名符其实的环保涂料。目前这类产品尚处于产业化前期，对经济社会的贡献率较低，但是，作为一种新技术，用于对新材料的研制与开发具有良好的发展前景。</p>

