						    <h3><strong>辐照技术在其他工业领域的应用</strong></h3>
						    <h4><strong>&nbsp;&nbsp;&nbsp;&nbsp;聚合物的辐射交联</strong></h4>
						    <p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;聚合物的辐射交联就是在一定剂量的辐射作用下，聚合物大分子间形成化学键（即横键或交联键），导致分子量增加，并使聚合物的线状分子互相结合，把线状分子结构转变成热固性聚合物那样的网状大分子结构。这样，聚合物原有的性质就会有很大的改变。例如辐射交联电线电缆、低温阻燃柔软热缩细管等。</p>
				     	    
						    <br/>

                            <h4><strong>&nbsp;&nbsp;&nbsp;&nbsp;聚乙烯泡沫塑料</strong></h4>
						    <p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;聚乙烯经辐射交联后才有一定强度，能将分解气体包埋住，产品具有独立的微细气孔，表面柔滑美观、加工性能好，并具有优良的耐腐蚀性、消冲击性、绝缘和绝热性能。</p>

						    <br/>

                            <h4><strong>&nbsp;&nbsp;&nbsp;&nbsp;辐照去污</strong></h4>
						    <p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;污水通常是采用“活性污泥法”进行处理的。由此产生的沉积物、淤渣泥浆需要进一步处理。污泥浆本身含有很多磷、氮等优质肥料。但是另一方面，人们也担心在污泥中隐藏了各种各样的细菌。因此，先要用射线进行辐照灭菌，然后才能用作肥料。</p>
				     	    
						    <br/>

                            <h4><strong>&nbsp;&nbsp;&nbsp;&nbsp;辐照致色</strong></h4>
						    <p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;辐射源发出的高能粒子穿过宝石晶体时,晶体内部会产生不同类型的点阵缺陷(空位、离位原子或离子),进而诱发新色心的形成,导致颜色改变。常见的有色宝石辐照处理有蓝宝石、托帕石、碧玺、长石、方柱石、锂辉石、方解石、珍珠和石英等</p>
				     	  
						    <br/>

                            <h4><strong>&nbsp;&nbsp;&nbsp;&nbsp;半导体器件辐照</strong></h4>
						    <p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;高能电子轰击半导体器件时,可穿透管壳或芯片进入硅体内,即能产生电离效应,又能产生位移效应。由于辐照电子与物质晶格相互作用,从而形成了空穴间隙原子对,破坏了晶格的位置,这些空位将进一步与杂质或其它空位作用而形成更复杂的缺陷,如氧空位对、磷空位对等。因而在禁带中形成了新的电子能级,不同的能级将根据其在禁带中的位置,对电子空穴俘获截面的大小和能级密度的大小均对非平衡载流子的复合有贡献, 从而引起少子寿命和载流子浓度的降低,结果使得快速二极管 trr、VF,可控硅 tq、VT等参数发生了变化。</p>
                            <p style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;与传统掺金工艺相比，采用高能电子辐照半导体器件具备如下特点：1)工艺可控性好,少子寿命可得到精确控制,器件参数的一致性及重复性好,成品率高,这是掺金工艺无法比拟的。特别是超快速器件采用掺金工艺是无法达到的。(2)加工简便、灵活性大.由于高能电子能量高、穿透力强可直接用于封装成形的器件,这大大简化了工艺和设计,避免了掺质工艺中的沾污，提高了器件的稳定性。(3)辐照后器件 trr-VF、 tq-VT折衷关系好。 (4)器件高温性能好,克服了掺金工艺的致命弱点。 (5)对掺金器件参数不合格品还可补照.同时还解决了人们最关心的辐照器件的稳定性问题。</p>
				        	
