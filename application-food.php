                <h4><strong>&nbsp;&nbsp;&nbsp;&nbsp;食品辐照的安全性</strong></h4>
                <ul>
                    <li><P style="color:#bbb">放射性物质残留: 食品在进行辐照时是外照射，没有直接接触放射性核素，因此，不会污染放射性物质，这与核爆炸和核源泄漏事故截然不同，故不存在残留问题。</P></li>
                    <li><P style="color:#bbb">毒性物质生成:未确证会产生有毒、致癌、和致畸物质。</P></li>
                    <li><P style="color:#bbb">微生物类变异:不会增加细菌、酵母菌和病毒的致病性。</P></li>
                    <li><P style="color:#bbb">营养物质破坏:低剂量辐射的营养损失微不足道。</P></li>
                    <li><P style="color:#bbb">放射性污染: 由电离辐射使食品中某些元素变成放射性元素。允许食品辐射的最大能量水平：电子射线：10 MeV；γ射线：5 MeV；x射线：5 MeV。能量都低于在食品中可能诱导放射性的能量阈值，当然也就不会产生诱导放射性核素及其化合物。</P></li>
                 
                </ul> 

                <h4><strong>&nbsp;&nbsp;&nbsp;&nbsp;与传统方法相比，辐照技术的优点</strong></h4>
                <P style="color:#bbb">辐照保藏不同于化学熏蒸法和腌制法，不需要加入添加物，与加热、冷冻等方法一样，是一种物理保藏法，且属于冷处理技术，具有许多传统保藏法不可比拟的优点:</P>

                <ul>
                    <li><P style="color:#bbb">射线穿透力强，可对预先包装好的或烹调好的食品通过剂量控制和辐照工艺进行均匀彻底处理，相比于热处理杀菌，辐照过程较易控制；</P></li>
                    <li><P style="color:#bbb">辐照处理是“冷加工”，可保持食品原有的鲜度和风味，有的甚至可提高食品的工艺质量；</P></li>
                    <li><P style="color:#bbb">辐照食品不会留下任何残留物，不污染环境，可提高食品卫生质量并有利于环保；</P></li>
                    <li><P style="color:#bbb">节省能源，与热处理、干燥和冷冻保藏食品法相比，能耗降低几倍到十几倍；</P></li>
                    <li><P style="color:#bbb">可对包装、捆扎好的食品进行杀菌处理，消除了在食品生产和制备过程中可能出现的严重交叉污染问题；</P></li>
                    <li><P style="color:#bbb">杀菌效果好，并可通过调整辐照剂量达到对各类食品杀菌的要求；</P></li>
                    <li><P style="color:#bbb">辐照灭菌速度快，操作简便，加工易控制，可连续加工，既经济，又省力，适于大规模加工。</P></li>
                </ul> 

