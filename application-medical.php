                      <ul>
                          <li><P style="color:#bbb"><label style="color:#ffe100">卫生材料</label>是以棉织物、无纺布等为材料制作的医用敷料、手术巾、手术衣、急救包和棉球等十几种卫生材料的总称。目前，我国的生产企业约有400多家，43%的企业生产的产品出口。</P></li>
