                                  <P style="color:#bbb">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;美国药典规定高剂量有效灭菌为 2.5 kGy，中剂量为 1 kGy，低剂量为 0.2～0.4 kGy。</P>
